the_weather = 'cold'

case the_weather
when 'sunny'
	puts "Don't forget your sunscreen!"
when 'cloudy'
	puts "The sun is behind the clouds."
when 'foggy'
	puts "Visibility is low."
when 'rainy'
	puts "Bring an umbrella."	
when 'cold' 
	puts "It's going to be chilly today."
else
	puts "I don't know what kind of weather that is!"	
end
